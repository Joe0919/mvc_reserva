    private void btnver_pswActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnver_pswActionPerformed
        // Suponiendo que tienes un JToggleButton llamado toggleButton_ver_clave
        if (btnver_psw.isSelected()) {
            String pass = "";
            char[] passIngresado = txpassword.getPassword();
            for (int i = 0; i < passIngresado.length; i++) {
                pass += passIngresado[i];
            }
            txpassword_visible.setText(pass);
            txpassword_visible.setVisible(true);
            txpassword.setVisible(false);
            isPswVisible = true;

            btnver_psw.setIcon(new ImageIcon(getClass().getResource("/files/icons/ojo-abierto.png")));
        } else {
            String passwordIngresado = txpassword_visible.getText();
            txpassword.setText(passwordIngresado);
            txpassword.setVisible(true);
            txpassword_visible.setVisible(false);
            isPswVisible = false;

            btnver_psw.setIcon(new ImageIcon(getClass().getResource("/files/icons/ojo-cerrado.png")));
        }
    }//GEN-LAST:event_btnver_pswActionPerformed

