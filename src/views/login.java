    private void ingreso() {

        if (isPswVisible) {
            txpassword.setText(txpassword_visible.getText());
        }
        
        if (txusuario.getText().trim().isEmpty() || String.valueOf(txpassword.getPassword()).length() < 8) {

            JOptionPane.showMessageDialog(this, "Complete sus credenciales correctamente.");
            if (txusuario.getText().trim().isEmpty()) {
                logoerrorusuario.setVisible(true);
                txerrorusuario.setVisible(true);
                jSeparator1.setBackground(Color.red);
            }
            if (String.valueOf(txpassword.getPassword()).length() < 8) {
                logoerrorpsw.setVisible(true);
                txerrorpsw.setVisible(true);
                jSeparator2.setBackground(Color.red);
            }
        } else {
            UsuarioController usuarioctrl = new UsuarioController();
            Usuario usuario = new Usuario();

            Persona persona = new Persona();
            persona.setDNI(txusuario.getText().trim());

            Empleado empleado = new Empleado();
            empleado.setPersona(persona);

            usuario.setEmpleado(empleado);
            usuario.setPassword(String.valueOf(txpassword.getPassword()).trim());

            if (usuarioctrl.loginUser(usuario)) {
                JOptionPane.showMessageDialog(null, "Login Correcto...");
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o contraseña incorrectos...");
            }
        }
    }

